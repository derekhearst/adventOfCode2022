function computeMove(knot) {
	if (knot.child) {
		let xDiff = Math.abs(knot.x - knot.child.x)
		let yDiff = Math.abs(knot.y - knot.child.y)
		//get distance between two points
		let distance = Math.sqrt(Math.pow(knot.x - knot.child.x, 2) + Math.pow(knot.y - knot.child.y, 2))
		if (distance > 1.5) {
			// console.log(distance)
			if (xDiff >= yDiff) {
				if (knot.child.x - knot.x >= 0) {
					knot.child.x = knot.x + 1
					knot.child.y = knot.y
				} else {
					knot.child.x = knot.x - 1

					knot.child.y = knot.y
				}
			} else if (yDiff >= xDiff) {
				if (knot.child.y - knot.y >= 0) {
					knot.child.y = knot.y + 1
					knot.child.x = knot.x
				} else {
					knot.child.y = knot.y - 1
					knot.child.x = knot.x
				}
			}
		}
		computeMove(knot.child)
	} else {
		tailKnotLocations.add(knot.x + "," + knot.y)
	}
}

function drawPast() {
	for (let knotLocation of tailKnotLocations) {
		let [x, y] = knotLocation.split(",").map(x => parseInt(x))
		ctx.beginPath()
		ctx.fillStyle = "purple"
		ctx.fillRect(x * scale + offset, y * scale + offset, scale, scale)
	}
}

function drawKnotLocationsSize() {
	ctx.beginPath()
	ctx.fillStyle = "white"
	ctx.font = "30px Arial"
	ctx.fillText(tailKnotLocations.size, 10, 30)
}

function drawKnots(root, first = false) {
	ctx.beginPath()

	if (root.child) {
		ctx.fillStyle = "red"
	} else {
		ctx.fillStyle = "green"
	}
	if (first) {
		ctx.fillStyle = "blue"
	}

	ctx.fillRect(root.x * scale + offset, root.y * scale + offset, scale, scale)
	if (root.child) {
		drawKnots(root.child)
	}
}

